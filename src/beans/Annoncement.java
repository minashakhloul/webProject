package beans;

public class Annoncement {
	
	private int idAnnoncement;
	private int idStudent;
	private String title;
	private int price;
	private String description;
	private String eventFilePath;
	public int getIdAnnoncement() {
		return idAnnoncement;
	}
	public void setIdAnnoncement(int idAnnoncement) {
		this.idAnnoncement = idAnnoncement;
	}
	public int getIdStudent() {
		return idStudent;
	}
	public void setIdStudent(int idStudent) {
		this.idStudent = idStudent;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public int getPrice() {
		return price;
	}
	public void setPrice(int price) {
		this.price = price;
	}
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}
	public String getEventFilePath() {
		return eventFilePath;
	}
	public void setEventFilePath(String eventFilePath) {
		this.eventFilePath = eventFilePath;
	}
	
	

}
